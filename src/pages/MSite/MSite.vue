<script>
import { mapState, mapActions } from 'vuex'

import HeaderTop from '../../components/HeaderTop/HeaderTop'
import ShopList from '../../components/ShopList/ShopList'
import Swiper from 'swiper'
export default {
  name: 'MSite',
  data () {
    return {
      mySwiper: {},
      baseImageUrl: 'http://fuss10.elemecdn.com'
    }
  },
  components: {
    HeaderTop,
    ShopList
  },
  mounted () {
    this.getCategorys()
    this.getShops()
  },
  computed: {
    ...mapState(['address', 'categorys']),

    /**
    根据categorys一维数组转换二维数组
    小数组中的元素个数最大是8
     */
    categoryArr () {
      const { categorys } = this
      const arr = []
      let minArr = []
      categorys.forEach(e => {
        minArr.push(e)
        if (minArr.length === 8) {
          arr.push(minArr)
          minArr = []
        }
      })
      return arr
    }
  },
  watch: {
    categorys (val) {
      this.$nextTick(() => { // 一旦完成界面更新, 立即调用
        this.mySwiper = new Swiper('.swiper-container', {
          loop: true, // 循环模式选项
          // 如果需要分页器
          pagination: {
            el: '.swiper-pagination'
          }
        })
      })
    }
  },
  methods: {
    ...mapActions(['getCategorys', 'getShops'])
  }
}
</script>

